	<script>
document.addEventListener('DOMContentLoaded', function() {
    const form = document.getElementById('contactForm');
    const formMessage = document.getElementById('formMessage');
    const submitButton = form.querySelector('button[type="submit"]');

    // Function to display messages
    function showMessage(message, type) {
        formMessage.style.display = 'block';
        formMessage.className = `alert alert-${type}`;
        formMessage.textContent = message;
        
        if (type === 'success') {
            setTimeout(() => {
                formMessage.style.display = 'none';
            }, 5000);
        }
    }

    // Function to handle form submission
    async function handleSubmit(e) {
        e.preventDefault();
        
        // Disable submit button and show loading state
        submitButton.disabled = true;
        submitButton.innerHTML = '<span class="spinner-border spinner-border-sm" role="status" aria-hidden="true"></span> Sending...';
        
        try {
            const formData = new FormData(form);
            
            const response = await fetch('dashboard/admin/contact_process.php', {
                method: 'POST',
                body: formData
            });

            if (!response.ok) {
                throw new Error(`HTTP error! status: ${response.status}`);
            }

            const data = await response.json();
            
            if (data.status === 'success') {
                showMessage(data.message, 'success');
                form.reset();
            } else {
                // Show specific error message
                let errorMessage = data.message;
                if (data.details) {
                    errorMessage += '\n' + Object.entries(data.details)
                        .map(([key, value]) => `${key}: ${value}`)
                        .join('\n');
                }
                showMessage(errorMessage, 'danger');
            }

        } catch (error) {
            showMessage('Connection error. Please check your Database and try again.', 'danger');
            console.error('Error:', error);
        } finally {
            // Re-enable submit button and restore original text
            submitButton.disabled = false;
            submitButton.textContent = 'Send Message';
        }
    }

    // Add form validation
    function validateForm() {
        const inputs = form.querySelectorAll('input, textarea');
        let isValid = true;

        inputs.forEach(input => {
            input.classList.remove('is-invalid');
            const errorDiv = input.nextElementSibling;
            if (errorDiv && errorDiv.classList.contains('invalid-feedback')) {
                errorDiv.remove();
            }

            if (input.hasAttribute('required') && !input.value.trim()) {
                isValid = false;
                input.classList.add('is-invalid');
                const div = document.createElement('div');
                div.className = 'invalid-feedback';
                div.textContent = `${input.placeholder} is required`;
                input.parentNode.insertBefore(div, input.nextSibling);
            }

            if (input.type === 'email' && input.value.trim()) {
                const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
                if (!emailRegex.test(input.value.trim())) {
                    isValid = false;
                    input.classList.add('is-invalid');
                    const div = document.createElement('div');
                    div.className = 'invalid-feedback';
                    div.textContent = 'Please enter a valid email address';
                    input.parentNode.insertBefore(div, input.nextSibling);
                }
            }
        });

        return isValid;
    }

    // Event listeners
    form.addEventListener('submit', function(e) {
        e.preventDefault();
        if (validateForm()) {
            handleSubmit(e);
        }
    });

    // Real-time validation on input
    form.querySelectorAll('input, textarea').forEach(input => {
        input.addEventListener('blur', () => {
            validateForm();
        });
        
        input.addEventListener('input', () => {
            if (input.classList.contains('is-invalid')) {
                validateForm();
            }
        });
    });
});
